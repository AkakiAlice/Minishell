#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*list;

	if (!lst || !f)
		return ;
	while (lst)
	{
		list = lst->next;
		f(lst->content);
		lst = list;
	}
}
