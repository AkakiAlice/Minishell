#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	str_size;
	int		i;

	i = 0;
	str_size = ft_strlen(s1) + 1;
	str = (char *)malloc(sizeof(char) * str_size);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
